def load_json(path: Path) -> dict:
    with path.open("r", encoding="utf-8") as f:
        return json.load(f)


def dump_json(path: Path, data: dict) -> None:
    formatted = json.dumps(data, ensure_ascii=False, indent=4)
    with path.open("w", encoding="utf-8") as f:
        f.write(formatted + "\n")


def main():
    base_dir = Path(__file__).parent
    for lang, mapping in translations.items():
        common_path = base_dir / lang / "common.json"
        if not common_path.exists():
            print(f"{lang}: common.json not found, skip")
            continue
        data = load_json(common_path)
        updated = 0
        for key, val in mapping.items():
            if key in TARGET_KEYS:
                if data.get(key) != val:
                    data[key] = val
                    updated += 1
        if updated > 0:
            dump_json(common_path, data)
            print(f"{lang}: updated {updated} keys")
        else:
            print(f"{lang}: no changes")


if __name__ == "__main__":
    main()

